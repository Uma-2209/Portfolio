function init(data) {
  buildMeta(data.meta, data.hero);
  buildHero(data.hero, data.meta);
  buildAbout(data.about);
  buildSkills(data.skills);
  buildProjects(data.projects);
  buildEducation(data.education);
  buildContact(data.contact);
  buildFooter(data.footer, data.contact);
  initNav();
  initScroll();
  initForm(data.contact);
  initBackToTop();
}

function buildMeta(meta, hero) {
  document.title = meta.siteTitle;
  const md = document.querySelector('#meta-desc');
  if(md) md.setAttribute('content', meta.siteDescription);
}

function buildHero(hero, meta) {
  setText('#hero-name', hero.name);
  setText('#hero-title', hero.title);
  setText('#hero-tagline', '"' + hero.tagline + '"');
  setText('#hero-location', hero.location);

  const resumeBtn = document.querySelector('#btn-resume');
  if (resumeBtn && meta.resumeUrl) {
    resumeBtn.href = meta.resumeUrl;
    resumeBtn.setAttribute('download', 'Uma_Bhargavi_Resume.pdf');
  }

  const navResume = document.querySelector('#nav-resume');
  if (navResume && meta.resumeUrl) {
    navResume.href = meta.resumeUrl;
    navResume.setAttribute('download', 'Uma_Bhargavi_Resume.pdf');
  }

  if (hero.photo) {
    const wrap = document.querySelector('#photo-inner');
    if(wrap) {
      const img = new Image();
      img.onload = function() {
        wrap.innerHTML = '';
        img.alt = hero.name;
        img.style.cssText = 'width:100%;height:100%;object-fit:cover;object-position:center top;border-radius:14px;';
        wrap.appendChild(img);
      };
      img.src = hero.photo;
    }
  }
}

function buildAbout(about) {
  const paraContainer = document.querySelector('#about-paragraphs');
  if(paraContainer) {
    about.paragraphs.forEach(p => {
      const el = document.createElement('p');
      el.textContent = p;
      paraContainer.appendChild(el);
    });
  }

  const grid = document.querySelector('#about-highlights');
  if(grid) {
    about.highlights.forEach(h => {
      grid.innerHTML += `
        <div class="highlight-card reveal">
          <i class="fa ${h.icon}"></i>
          <h4>${h.label}</h4>
          <span>${h.value}</span>
        </div>`;
    });
  }
}

